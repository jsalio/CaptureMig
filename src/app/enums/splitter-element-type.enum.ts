/**
 * Represents a splitter element type.
 * @export
 * @enum {number}
 */
export enum SplitterElementType {

    /**
     * Represents the elment type for a document handler.
     */
    DocumentHandler = 'DocumentHandler',

    /**
     * Represents an document type's type.
     */
    DocumentType = 'DocumentType',

    /**
     * Represents the elment type for an element value.
     */
    ElementValue = 'ElementValue',

    /**
     * Represents the elment type for a fixed value.
     */
    BarcodeHandler = 'BarcodeHandler',

    /**
     * Represents the elment type for a page number.
     */
    PageNumber = 'PageNumber',

    /**
     * Represents the elment type for a total page.
     */
    TotalPage = 'TotalPage'
}
