/**
 * Represents the form of the document view
 *
 * @export
 * @enum {number}
 */
export enum DocumentViewMode {
  /**
   * Represents the document view mode when only display thumbnail of the pages
   */
  thumbnail = 'thumbnail',
  /**
   * Represents the document view mode when display the document as a list of pages with the big image
   */
  document = 'document',
}
