/**
 * Represent orientation of reader page
 * @export
 * @enum {number}
 */
export enum ReaderOrientation {
/**
 * Use this value to read the page from right to left or from left to right
 */
Horizontal = 'Horizontal',

/**
 * Use this value to read the page from top to bottom or from bottom to top
 */
Vertical = 'Vertical'
}
