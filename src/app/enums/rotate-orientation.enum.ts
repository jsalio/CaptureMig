/**
 * Represent a rotation orientation
 * @export
 * @enum {number}
 */
export enum RotateOrientation {
    /**
     * Represents an invalid status
     */
    None = 0,

    /**
     * Use this value to rotate a page to left
     */
    Left = 1,

    /**
     * Use this value to rotate a page to right
     */
    Right = 2
}
