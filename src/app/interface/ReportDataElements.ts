/**
 * Represents a simple report column data in form {label, value}
 * @export
 * @interface ReportData
 */
export interface ReportDataElements {
  /**
   * represents a display label text
   * @type {string}
   * @memberof ReportData
   */
  label: string;
  /**
   * represents a value.
   * @type {number}
   * @memberof ReportData
   */
  value: number;
}
